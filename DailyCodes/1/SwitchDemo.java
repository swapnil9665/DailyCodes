class Switch{

public static void main(String args[]){
	String str="two";
	switch(str)
	{
		case "one" :
			System.out.println("cpp");
			break;
		case "two" :
			System.out.println("java");
			break;
		case "three" :
			System.out.println("DSA");
			break;

		default:
			System.out.println("invalid input!");

	
	
	}
}
}
